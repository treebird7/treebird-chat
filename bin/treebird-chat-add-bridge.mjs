#!/usr/bin/env node

import { existsSync, writeFileSync } from 'node:fs';
import { spawn, spawnSync } from 'node:child_process';
import { dirname, relative, resolve, sep } from 'node:path';
import { fileURLToPath } from 'node:url';
import { readAcl } from '../lib/access.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '..');
const ALLOW_BIN = resolve(__dirname, 'treebird-chat-allow.mjs');
const CORRWAIT_BIN = resolve(__dirname, 'corrwait.mjs');

function parseArgs(argv) {
  const args = { kind: null, url: null, as: null, model: null, file: null };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--kind') args.kind = argv[++i];
    else if (a === '--url') args.url = argv[++i];
    else if (a === '--as') args.as = argv[++i];
    else if (a === '--model') args.model = argv[++i];
    else if (!a.startsWith('--') && !args.file) args.file = a;
  }
  return args;
}

function usage() {
  process.stderr.write(
    'usage: treebird-chat-add-bridge --kind <gemma|memosan|generic-http> --url <endpoint> --as <agent> [--model <model>] <file>\n'
  );
}

function toImportSpecifier(fromDir, absolutePath) {
  let specifier = relative(fromDir, absolutePath).split(sep).join('/');
  if (!specifier.startsWith('.')) specifier = `./${specifier}`;
  return specifier;
}

function bridgeHeader() {
  return '#!/usr/bin/env node\n';
}

function gemmaTemplate({ agent, defaultUrl, defaultModel, accessImport, mentionImport, corrwaitPath }) {
  return `${bridgeHeader()}// Generated by treebird-chat-add-bridge

import { resolve } from 'node:path';
import { existsSync, readFileSync } from 'node:fs';
import { spawn } from 'node:child_process';
import { isAllowed } from '${accessImport}';
import { scanForMentions } from '${mentionImport}';

const CORRWAIT = ${JSON.stringify(corrwaitPath)};

function parseArgs(argv) {
  const args = {
    file: null,
    url: ${JSON.stringify(defaultUrl || 'http://localhost:1234')},
    model: ${JSON.stringify(defaultModel || 'google/gemma-4-27b-it')},
    as: ${JSON.stringify(agent)},
    contextLines: 30,
  };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--url') args.url = argv[++i];
    else if (a === '--model') args.model = argv[++i];
    else if (a === '--as') args.as = argv[++i];
    else if (a === '--context') args.contextLines = parseInt(argv[++i], 10);
    else if (!a.startsWith('--') && !args.file) args.file = a;
  }
  return args;
}

function nowHHMM() {
  const d = new Date();
  return \`\${String(d.getHours()).padStart(2, '0')}:\${String(d.getMinutes()).padStart(2, '0')}\`;
}

function log(msg) {
  process.stderr.write(\`[\${nowHHMM()} ${agent}-bridge] \${msg}\\n\`);
}

function getContext(filePath, n) {
  const lines = readFileSync(filePath, 'utf8').split('\\n').filter((line) => line.trim());
  return lines.slice(-n).join('\\n');
}

function splitReply(text) {
  return text
    .split('\\n')
    .flatMap((line) => {
      if (line.length <= 200) return [line];
      const parts = [];
      for (let i = 0; i < line.length; i += 200) parts.push(line.slice(i, i + 200));
      return parts;
    })
    .filter((line) => line.trim());
}

function runCorrwait(filePath, agentName) {
  return new Promise((resolveResult) => {
    const child = spawn(process.execPath, [
      CORRWAIT,
      filePath,
      '--as',
      agentName,
      '--timeout',
      '540',
      '--on-mention',
    ], {
      stdio: ['ignore', 'pipe', 'inherit'],
    });
    let out = '';
    child.stdout.on('data', (chunk) => { out += chunk.toString(); });
    child.on('close', () => {
      try { resolveResult(JSON.parse(out.trim())); }
      catch { resolveResult({ reason: 'ERROR' }); }
    });
  });
}

function appendLine(filePath, agentName, message) {
  return new Promise((resolveResult, reject) => {
    const child = spawn(process.execPath, [
      CORRWAIT,
      filePath,
      '--as',
      agentName,
      '--write',
      message,
    ], {
      stdio: ['ignore', 'ignore', 'pipe'],
    });
    let stderr = '';
    child.stderr.on('data', (chunk) => { stderr += chunk.toString(); });
    child.on('close', (code) => {
      if (code === 0) resolveResult();
      else reject(new Error(stderr.trim() || \`corrwait --write exited \${code}\`));
    });
  });
}

async function appendReply(filePath, agentName, text) {
  for (const line of splitReply(text)) {
    await appendLine(filePath, agentName, line);
  }
}

const SYSTEM_PROMPT = \`You are ${agent}, a local AI model participating in a treebird-chat session. \\
You can see recent chat history for context. Respond directly and concisely — this is a live chat, \\
not a document. Keep replies short (2–4 sentences) unless a technical question genuinely needs more. \\
Use plain text. No markdown headers. No preamble like "Sure!" or "Of course!".\`;

async function callModel(baseUrl, model, context, question) {
  const userMsg = \`Recent chat context:\\n\${context}\\n\\nMessage addressed to you:\\n\${question}\`;
  const payload = JSON.stringify({
    model,
    messages: [
      { role: 'system', content: SYSTEM_PROMPT },
      { role: 'user', content: userMsg },
    ],
    temperature: 0.3,
    max_tokens: 512,
  });

  const url = new URL('/v1/chat/completions', baseUrl).toString();
  const res = await fetch(url, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: payload,
    signal: AbortSignal.timeout(120_000),
  });
  if (!res.ok) throw new Error(\`HTTP \${res.status}\`);
  const data = await res.json();
  return data.choices[0].message.content.trim();
}

async function main() {
  const { file, url, model, as: agentName, contextLines } = parseArgs(process.argv.slice(2));

  if (!file) {
    process.stderr.write('usage: ${agent}-bridge <file> [--url URL] [--model ID] [--as ${agent}] [--context 30]\\n');
    process.exit(1);
  }

  const filePath = resolve(file);
  if (!existsSync(filePath)) {
    process.stderr.write(\`File not found: \${filePath}\\n\`);
    process.exit(1);
  }

  process.env.BIRDCHAT_AGENT = agentName;

  if (!isAllowed(filePath, agentName)) {
    process.stderr.write(\`"\${agentName}" not in ACL. Run: treebird-chat-allow \${file} \${agentName}\\n\`);
    process.exit(1);
  }

  log(\`ready — file: \${file}\`);
  log(\`endpoint: \${url}  model: \${model}\`);

  while (true) {
    const result = await runCorrwait(filePath, agentName);

    if (result.reason === 'END' || result.reason === 'REVOKED') {
      log(\`exiting: \${result.reason}\`);
      break;
    }
    if (result.reason === 'TIMEOUT') continue;

    if (result.reason === 'ERROR') {
      log('corrwait exited with error — backing off 10s before retry');
      await new Promise((resolveDelay) => setTimeout(resolveDelay, 10_000));
      continue;
    }

    if (result.reason !== 'WAKE') {
      log(\`unexpected corrwait reason: \${result.reason} — continuing\`);
      continue;
    }

    const newLines = (result.newContent || '').split('\\n');
    const { mentions } = scanForMentions(newLines, agentName, 0);
    if (mentions.length === 0) continue;

    const question = mentions.map((mention) => \`[\${mention.time} \${mention.author}] \${mention.text}\`).join('\\n');
    const context = getContext(filePath, contextLines);

    log(\`@mention from \${mentions[0].author} — querying endpoint\`);
    try {
      const reply = await callModel(url, model, context, question);
      await appendReply(filePath, agentName, reply);
      log(\`replied (\${reply.length} chars)\`);
    } catch (error) {
      log(\`endpoint error: \${error.message}\`);
      await appendReply(filePath, agentName, \`(unavailable — \${error.message})\`);
    }
  }
}

async function supervisor() {
  while (true) {
    try {
      await main();
      log('bridge exited normally');
      break;
    } catch (error) {
      log(\`bridge crashed: \${error.stack || error.message} — restarting in 5s\`);
      await new Promise((resolveDelay) => setTimeout(resolveDelay, 5_000));
    }
  }
}

supervisor();
`;
}

function stubTemplate({ agent, kind }) {
  return `${bridgeHeader()}// Generated by treebird-chat-add-bridge

import { resolve } from 'node:path';
import { existsSync } from 'node:fs';

function parseArgs(argv) {
  const args = { file: null, url: null, model: null, as: ${JSON.stringify(agent)} };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--url') args.url = argv[++i];
    else if (a === '--model') args.model = argv[++i];
    else if (a === '--as') args.as = argv[++i];
    else if (!a.startsWith('--') && !args.file) args.file = a;
  }
  return args;
}

const { file } = parseArgs(process.argv.slice(2));
if (!file) {
  process.stderr.write('usage: ${agent}-bridge <file> [--url URL] [--model ID] [--as ${agent}]\\n');
  process.exit(1);
}

const filePath = resolve(file);
if (!existsSync(filePath)) {
  process.stderr.write(\`File not found: \${filePath}\\n\`);
  process.exit(1);
}

process.stdout.write('${kind} kind not yet fully implemented\\n');
setInterval(() => {}, 1 << 30);
`;
}

function renderTemplate(kind, options) {
  if (kind === 'gemma') return gemmaTemplate(options);
  if (kind === 'memosan' || kind === 'generic-http') return stubTemplate(options);
  throw new Error(`Unsupported kind: ${kind}`);
}

async function main() {
  const { kind, url, as: agent, model, file } = parseArgs(process.argv.slice(2));
  if (!kind || !url || !agent || !file) {
    usage();
    process.exit(1);
  }

  const AGENT_RE = /^[A-Za-z][A-Za-z0-9_-]{0,31}$/;
  if (!AGENT_RE.test(agent)) {
    process.stderr.write(`Invalid --as value: "${agent}". Must be letters/digits/hyphens/underscores, max 32 chars.\n`);
    process.exit(1);
  }

  const filePath = resolve(file);
  if (!existsSync(filePath)) {
    process.stderr.write(`File not found: ${filePath}\n`);
    process.exit(1);
  }

  const acl = readAcl(filePath);
  if (!acl) {
    process.stderr.write(`No ACL at ${filePath}.access.json. Run treebird-chat-allow first.\n`);
    process.exit(1);
  }

  const scriptPath = resolve(`${agent}-bridge.mjs`);
  const pidPath = resolve(`${agent}-bridge.pid`);
  const scriptDir = dirname(scriptPath);
  const template = renderTemplate(kind, {
    agent,
    kind,
    defaultUrl: url,
    defaultModel: model,
    accessImport: toImportSpecifier(scriptDir, resolve(REPO_ROOT, 'lib/access.mjs')),
    mentionImport: toImportSpecifier(scriptDir, resolve(REPO_ROOT, 'lib/mention-scanner.mjs')),
    corrwaitPath: CORRWAIT_BIN,
  });

  writeFileSync(scriptPath, template, { mode: 0o755 });

  const allow = spawnSync(process.execPath, [ALLOW_BIN, filePath, agent], {
    stdio: 'inherit',
  });
  if (allow.status !== 0) {
    process.exit(allow.status ?? 1);
  }

  const childArgs = [scriptPath, filePath, '--as', agent];
  if (url) childArgs.push('--url', url);
  if (model) childArgs.push('--model', model);

  const bridgeEnv = Object.fromEntries(
    Object.entries(process.env).filter(([k]) => !/^ENVOAK_|^SUPABASE_/.test(k))
  );

  const child = spawn(process.execPath, childArgs, {
    detached: true,
    stdio: 'ignore',
    env: bridgeEnv,
  });
  child.on('error', (err) => {
    process.stderr.write(`Failed to start ${agent}-bridge: ${err.message}\n`);
  });
  child.unref();

  writeFileSync(pidPath, `${child.pid}\n`, { mode: 0o644 });

  if (kind !== 'gemma') {
    process.stdout.write(`${kind} kind not yet fully implemented; started stub bridge.\n`);
  }
  process.stdout.write(
    `Started ${agent}-bridge (PID ${child.pid}). Join with: treebird-chat ${file} --as <yourname>\n`
  );
}

main().catch((error) => {
  process.stderr.write(`treebird-chat-add-bridge error: ${error.stack || error.message}\n`);
  process.exit(1);
});
